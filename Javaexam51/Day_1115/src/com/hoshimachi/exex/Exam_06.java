public class Exam_06 {

	public static void main(String[] args) {
		for(int x=0,y=5;x<5; x++, y--) {
			System.out.println("x = "+ x +"\ty="+y);
		}
		System.out.println("-------------");
		for(int i=0,e=0;i<3&&e<5; i++, e++) {
			System.out.println("x = "+ i +"\ty="+e);
		}
	}

}
